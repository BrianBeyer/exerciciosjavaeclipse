package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 3; //colocando var, o java vai definir o tipo de variavel
		System.out.println(b);
		
		var c = "Ola";
		System.out.println(c);
		
		double d;// outra forma de declarar a variavel
		d = 123.4;
		System.out.println(d);
			
	}
}
